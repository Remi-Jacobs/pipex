#include "libft.h"

long	ft_atol(const char *str)
{
	int		i;
	long	answer;
	long	neg;

	i = 0;
	answer = 0;
	neg = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		neg = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		answer = (answer * 10) + (str[i] - 48);
		i++;
	}
	return (answer * neg);
}
